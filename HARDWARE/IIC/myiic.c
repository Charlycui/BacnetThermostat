#include "myiic.h"
#include "delay.h"

#include "gpio_define.h"


//IO方向设置
void SDA_IN(void)
{
	GPIO_InitTypeDef GPIO_InitStructure; 
	GPIO_InitStructure.GPIO_Pin = gpio_map[EEP_SDA].GPIO_Pin_X;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPU; 
	GPIO_Init(gpio_map[EEP_SDA].GPIOX, &GPIO_InitStructure);
	GPIO_SetBits(gpio_map[EEP_SDA].GPIOX, gpio_map[EEP_SDA].GPIO_Pin_X);
}
void SDA_OUT(void)
{
	GPIO_InitTypeDef GPIO_InitStructure; 
	GPIO_InitStructure.GPIO_Pin = gpio_map[EEP_SDA].GPIO_Pin_X;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(gpio_map[EEP_SDA].GPIOX, &GPIO_InitStructure);
	GPIO_SetBits(gpio_map[EEP_SDA].GPIOX, gpio_map[EEP_SDA].GPIO_Pin_X);
}

//IO操作函数	 
static void IIC_SCL(u8 status)
{
	if(status) 
		GPIO_SetBits(gpio_map[EEP_SCL].GPIOX, gpio_map[EEP_SCL].GPIO_Pin_X);
	else
		GPIO_ResetBits(gpio_map[EEP_SCL].GPIOX, gpio_map[EEP_SCL].GPIO_Pin_X); 
}
static void IIC_SDA(u8 status)
{
	if(status) 
		GPIO_SetBits(gpio_map[EEP_SDA].GPIOX, gpio_map[EEP_SDA].GPIO_Pin_X);
	else
		GPIO_ResetBits(gpio_map[EEP_SDA].GPIOX, gpio_map[EEP_SDA].GPIO_Pin_X); 
}	
u8 READ_SDA(void)
{
	u8 status;
	status = GPIO_ReadInputDataBit(gpio_map[EEP_SDA].GPIOX, gpio_map[EEP_SDA].GPIO_Pin_X);  		  
	return status;
}

//初始化IIC
void IIC_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	
	RCC_APB2PeriphClockCmd(gpio_map[EEP_SCL].RCC_APB2Periph_X, ENABLE);//使能SCL
	GPIO_InitStructure.GPIO_Pin = gpio_map[EEP_SCL].GPIO_Pin_X ;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
 	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(gpio_map[EEP_SCL].GPIOX, &GPIO_InitStructure);
	GPIO_SetBits(gpio_map[EEP_SCL].GPIOX, gpio_map[EEP_SCL].GPIO_Pin_X );
	
	RCC_APB2PeriphClockCmd(gpio_map[EEP_SDA].RCC_APB2Periph_X, ENABLE);//使能SDL 
	GPIO_InitStructure.GPIO_Pin =  gpio_map[EEP_SDA].GPIO_Pin_X;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(gpio_map[EEP_SDA].GPIOX, &GPIO_InitStructure);
	GPIO_SetBits(gpio_map[EEP_SDA].GPIOX,  gpio_map[EEP_SDA].GPIO_Pin_X);
}

//产生IIC起始信号
void IIC_Start(void)
{
	SDA_OUT();		//sda线输出
	IIC_SDA(1);	  	  
	IIC_SCL(1);
	delay_us(4);
 	IIC_SDA(0);	//START:when CLK is high,DATA change form high to low 
	delay_us(4);
	IIC_SCL(0);	//钳住I2C总线，准备发送或接收数据 
}
	  
//产生IIC停止信号
void IIC_Stop(void)
{
	SDA_OUT();		//sda线输出
	IIC_SCL(0);
	IIC_SDA(0);	//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	IIC_SCL(1); 
	IIC_SDA(1);	//发送I2C总线结束信号
	delay_us(4);							   	
}

//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
u8 IIC_Wait_Ack(void)
{
	u16 ucErrTime = 0;
	IIC_SDA(1);
	SDA_IN();		//SDA设置为输入  
	
	delay_us(1);	   
	IIC_SCL(1);
	delay_us(1);	 
	while(READ_SDA())
	{
		ucErrTime++;
		if(ucErrTime > 250)
		{
			IIC_Stop();
			return 1;
		}
	}
	IIC_SCL(0);	//时钟输出0 	   
	return 0;  
}
 
//产生ACK应答
void IIC_Ack(void)
{
	IIC_SCL(0);
	SDA_OUT();
	IIC_SDA(0);
	delay_us(2);
	IIC_SCL(1);
	delay_us(2);
	IIC_SCL(0);
}

//不产生ACK应答		    
void IIC_NAck(void)
{
	IIC_SCL(0);
	SDA_OUT();
	IIC_SDA(1);
	delay_us(2);
	IIC_SCL(1);
	delay_us(2);
	IIC_SCL(0);
}
					 				     
//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答			  
void IIC_Send_Byte(u8 txd)
{                        
    u8 t;   
	SDA_OUT(); 	    
    IIC_SCL(0);		//拉低时钟开始数据传输
    for(t = 0; t < 8; t++)
    {              
        IIC_SDA((txd & 0x80) >> 7);
        txd <<= 1; 	  
		delay_us(2);   //对TEA5767这三个延时都是必须的
		IIC_SCL(1);
		delay_us(2); 
		IIC_SCL(0);	
		delay_us(2);
    }	 
}
	    
//读1个字节，ack=1时，发送ACK，ack=0，发送nACK   
u8 IIC_Read_Byte(unsigned char ack)
{
	unsigned char i, receive = 0;

	SDA_IN();			//SDA设置为输入

    for(i = 0; i < 8; i++)
	{
        IIC_SCL(0); 
        delay_us(2);
		IIC_SCL(1);
        receive <<= 1;
        if(READ_SDA())receive++;   
		delay_us(1); 
    }
						 
    if(!ack)
        IIC_NAck();//发送nACK
    else
        IIC_Ack(); //发送ACK
		   
    return receive;
}
